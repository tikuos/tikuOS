static inline void stm32f411_gpio_set_af(uint32_t gpio_base,
                                         uint8_t pin,
                                         uint8_t af) {
    uint32_t reg = (pin < 8U) ? STM32F411_GPIO_AFRL(gpio_base)
                              : STM32F411_GPIO_AFRH(gpio_base);
    uint32_t shift = (uint32_t)(pin & 7U) * 4U;
    uint32_t val = _STM32F411_REG(reg);
    val &= ~(0x0FUL << shift);
    val |= ((uint32_t)af & 0x0FU) << shift;
    _STM32F411_REG(reg) = val;
}

